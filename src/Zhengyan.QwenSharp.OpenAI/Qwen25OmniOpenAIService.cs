    private static (byte[] AudioBytes, string ContentType) ConvertSpeechOutput(byte[] wavBytes, string responseFormat)
    {
        return responseFormat switch
        {
            "wav" => (wavBytes, "audio/wav"),
            "pcm" => (ExtractPcm16FromWav(wavBytes), "application/octet-stream"),
            _ => throw new InvalidOperationException($"Unsupported response_format '{responseFormat}'. Supported values: wav, pcm."),
        };
    }

    private static byte[] ExtractPcm16FromWav(byte[] wavBytes)
    {
        if (wavBytes.Length < 44)
        {
            throw new InvalidOperationException("Invalid WAV payload.");
        }

        using var stream = new MemoryStream(wavBytes, writable: false);
        using var reader = new BinaryReader(stream, Encoding.ASCII, leaveOpen: false);
        stream.Seek(12, SeekOrigin.Begin);

        while (stream.Position + 8 <= stream.Length)
        {
            var chunkId = Encoding.ASCII.GetString(reader.ReadBytes(4));
            var chunkSize = reader.ReadInt32();
            if (chunkSize < 0 || stream.Position + chunkSize > stream.Length)
            {
                throw new InvalidOperationException("Invalid WAV chunk size.");
            }

            if (string.Equals(chunkId, "data", StringComparison.Ordinal))
            {
                return reader.ReadBytes(chunkSize);
            }

            stream.Seek(chunkSize, SeekOrigin.Current);
            if ((chunkSize & 1) != 0 && stream.Position < stream.Length)
            {
                stream.Seek(1, SeekOrigin.Current);
            }
        }

        throw new InvalidOperationException("WAV payload does not contain a data chunk.");
    }

    private static float[] ApplyVoiceProfile(float[] samples, VoiceProfile profile)
    {
        if (samples.Length == 0)
        {
            return samples;
        }

        var voiced = samples;
        if (MathF.Abs(profile.PitchScale - 1f) > 0.0001f)
        {
            var pitchedLength = Math.Max(1, (int)Math.Round(samples.Length / profile.PitchScale));
            voiced = ResampleLinear(samples, pitchedLength);
            voiced = ResampleLinear(voiced, samples.Length);
        }

        if (MathF.Abs(profile.Gain - 1f) > 0.0001f)
        {
            for (var i = 0; i < voiced.Length; i++)
            {
                var sample = voiced[i] * profile.Gain;
                voiced[i] = Math.Clamp(sample, -1f, 1f);
            }
        }

        return voiced;
    }

    private static float[] ResampleLinear(float[] samples, int targetLength)
    {
        if (samples.Length == 0 || targetLength <= 0 || samples.Length == targetLength)
        {
            return samples;
        }

        if (samples.Length == 1)
        {
            return Enumerable.Repeat(samples[0], targetLength).ToArray();
        }

        var output = new float[targetLength];
        var ratio = (samples.Length - 1) / (double)(targetLength - 1);
        for (var i = 0; i < targetLength; i++)
        {
            var sourceIndex = i * ratio;
            var index0 = (int)Math.Floor(sourceIndex);
            var index1 = Math.Min(index0 + 1, samples.Length - 1);
            var t = (float)(sourceIndex - index0);
            output[i] = samples[index0] * (1f - t) + samples[index1] * t;
        }

        return output;
    }

