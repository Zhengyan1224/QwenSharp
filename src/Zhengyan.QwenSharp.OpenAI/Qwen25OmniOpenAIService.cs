    private static IReadOnlyList<OpenAIMessage> BuildToolAwareMessages(
        IReadOnlyList<OpenAIMessage> messages,
        IReadOnlyList<OpenAITool>? tools,
        JsonElement? toolChoice)
    {
        if (tools is null || tools.Count == 0 || ToolChoiceIsNone(toolChoice))
        {
            return messages;
        }

        var requiredToolName = ResolveRequiredToolName(toolChoice);
        var instruction = new StringBuilder();
        instruction.AppendLine("You have access to the following tools.");
        instruction.AppendLine("When a tool is needed, respond only with JSON in this exact shape:");
        instruction.AppendLine("""{"tool_calls":[{"name":"tool_name","arguments":{}}]}""");
        instruction.AppendLine("When no tool is needed, answer normally without JSON.");
        if (!string.IsNullOrWhiteSpace(requiredToolName))
        {
            instruction.AppendLine($"You must call the tool named '{requiredToolName}'.");
        }
        else if (ToolChoiceIsRequired(toolChoice))
        {
            instruction.AppendLine("You must call one of the provided tools.");
        }

        instruction.AppendLine("Tools:");
        instruction.AppendLine(JsonSerializer.Serialize(tools, new JsonSerializerOptions
        {
            WriteIndented = false,
            Encoder = System.Text.Encodings.Web.JavaScriptEncoder.UnsafeRelaxedJsonEscaping,
        }));

        var list = messages.ToList();
        list.Insert(0, new OpenAIMessage
        {
            Role = "system",
            Content = instruction.ToString(),
        });
        return list;
    }

    private static IReadOnlyList<OpenAIToolCall> TryParseToolCalls(string text, IReadOnlyList<OpenAITool>? tools)
    {
        if (tools is null || tools.Count == 0 || string.IsNullOrWhiteSpace(text))
        {
            return Array.Empty<OpenAIToolCall>();
        }

        var payload = ExtractJsonPayload(text);
        if (string.IsNullOrWhiteSpace(payload))
        {
            return Array.Empty<OpenAIToolCall>();
        }

        try
        {
            using var document = JsonDocument.Parse(payload);
            var root = document.RootElement;
            var toolNames = tools
                .Where(tool => string.Equals(tool.Type, "function", StringComparison.OrdinalIgnoreCase))
                .Select(tool => tool.Function?.Name)
                .Where(name => !string.IsNullOrWhiteSpace(name))
                .Select(name => name!)
                .ToHashSet(StringComparer.Ordinal);
            var parsed = new List<OpenAIToolCall>();

            if (root.TryGetProperty("tool_calls", out var toolCalls) && toolCalls.ValueKind == JsonValueKind.Array)
            {
                foreach (var item in toolCalls.EnumerateArray())
                {
                    var toolCall = ParseToolCall(item, toolNames);
                    if (toolCall is not null)
                    {
                        parsed.Add(toolCall);
                    }
                }
            }
            else
            {
                var toolCall = ParseToolCall(root, toolNames);
                if (toolCall is not null)
                {
                    parsed.Add(toolCall);
                }
            }

            return parsed;
        }
        catch (JsonException)
        {
            return Array.Empty<OpenAIToolCall>();
        }
    }

    private static OpenAIToolCall? ParseToolCall(JsonElement element, HashSet<string> allowedToolNames)
    {
        JsonElement source = element;
        if (element.TryGetProperty("function", out var functionElement) && functionElement.ValueKind == JsonValueKind.Object)
        {
            source = functionElement;
        }

        var name = ReadString(source, "name");
        if (string.IsNullOrWhiteSpace(name))
        {
            return null;
        }

        if (allowedToolNames.Count > 0 && !allowedToolNames.Contains(name))
        {
            return null;
        }

        var arguments = "{}";
        if (source.TryGetProperty("arguments", out var argumentsElement))
        {
            arguments = argumentsElement.ValueKind == JsonValueKind.String
                ? argumentsElement.GetString() ?? "{}"
                : argumentsElement.GetRawText();
        }

        return new OpenAIToolCall
        {
            Id = ReadString(element, "id") ?? $"call_{Guid.NewGuid():N}",
            Function = new OpenAIFunctionCall
            {
                Name = name,
                Arguments = string.IsNullOrWhiteSpace(arguments) ? "{}" : arguments,
            },
        };
    }

    private static string? ExtractJsonPayload(string text)
    {
        var trimmed = text.Trim();
        if (trimmed.StartsWith("```", StringComparison.Ordinal))
        {
            var firstNewline = trimmed.IndexOf('\n');
            var lastFence = trimmed.LastIndexOf("```", StringComparison.Ordinal);
            if (firstNewline >= 0 && lastFence > firstNewline)
            {
                trimmed = trimmed[(firstNewline + 1)..lastFence].Trim();
            }
        }

        if (trimmed.StartsWith('{') && trimmed.EndsWith('}'))
        {
            return trimmed;
        }

        var start = trimmed.IndexOf('{');
        var end = trimmed.LastIndexOf('}');
        return start >= 0 && end > start
            ? trimmed[start..(end + 1)]
            : null;
    }

    private static bool ToolChoiceIsNone(JsonElement? toolChoice)
        => toolChoice is not null
           && toolChoice.Value.ValueKind == JsonValueKind.String
           && string.Equals(toolChoice.Value.GetString(), "none", StringComparison.OrdinalIgnoreCase);

    private static bool ToolChoiceIsRequired(JsonElement? toolChoice)
        => toolChoice is not null
           && toolChoice.Value.ValueKind == JsonValueKind.String
           && string.Equals(toolChoice.Value.GetString(), "required", StringComparison.OrdinalIgnoreCase);

    private static string? ResolveRequiredToolName(JsonElement? toolChoice)
    {
        if (toolChoice is null || toolChoice.Value.ValueKind != JsonValueKind.Object)
        {
            return null;
        }

        var root = toolChoice.Value;
        if (root.TryGetProperty("function", out var function) &&
            function.ValueKind == JsonValueKind.Object)
        {
            return ReadString(function, "name");
        }

        return ReadString(root, "name");
    }

    private static string? ReadString(JsonElement element, string propertyName)
        => element.TryGetProperty(propertyName, out var property) && property.ValueKind == JsonValueKind.String
            ? property.GetString()
            : null;

