    private static IReadOnlyList<OpenAIMessage> ReplaceAudioMessagesWithTranscript(
        IReadOnlyList<OpenAIMessage> messages,
        string transcript)
    {
        if (string.IsNullOrWhiteSpace(transcript))
        {
            return messages;
        }

        var rewritten = new List<OpenAIMessage>(messages.Count);
        foreach (var message in messages)
        {
            if (message.Parts is null || message.Parts.Count == 0)
            {
                rewritten.Add(message);
                continue;
            }

            var parts = new List<OpenAIContentPart>();
            var text = new StringBuilder();
            foreach (var part in message.Parts)
            {
                if (string.Equals(part.Type, "input_audio", StringComparison.OrdinalIgnoreCase))
                {
                    continue;
                }

                if (string.Equals(part.Type, "text", StringComparison.OrdinalIgnoreCase) && !string.IsNullOrWhiteSpace(part.Text))
                {
                    text.Append(part.Text);
                }
                else
                {
                    parts.Add(part);
                }
            }

            if (message.Parts.Any(part => string.Equals(part.Type, "input_audio", StringComparison.OrdinalIgnoreCase)))
            {
                if (text.Length > 0)
                {
                    text.Append(' ');
                }

                text.Append(transcript);
                rewritten.Add(new OpenAIMessage
                {
                    Role = message.Role,
                    Content = text.ToString(),
                    Parts = parts.Count > 0 ? parts : null,
                });
            }
            else
            {
                rewritten.Add(message);
            }
        }

        return rewritten;
    }

    private static bool MessageContainsAudio(OpenAIMessage message)
        => message.Parts?.Any(part => string.Equals(part.Type, "input_audio", StringComparison.OrdinalIgnoreCase)) == true;

    private static bool IsDegenerateModelText(string text)
    {
        var trimmed = text.Trim();
        if (string.IsNullOrEmpty(trimmed))
        {
            return true;
        }

        if (trimmed.Length <= 16 && trimmed.StartsWith('<') && trimmed.EndsWith('>'))
        {
            return true;
        }

        return trimmed.All(ch => !char.IsLetterOrDigit(ch) && !IsCjk(ch));
    }

    private static bool IsCjk(char ch) => ch >= '\u3400' && ch <= '\u9fff';

    private int[] BuildVisionPromptIds(Qwen25OmniMmInfo mmInfo)
    {
        var messages = mmInfo.Messages;
        var imageIndex = 0;
        var videoIndex = 0;
        var ids = new List<int>();
        ids.Add(_tokenizer.ImStartId);
        ids.AddRange(_tokenizer.Encode("system\n", addSpecialTokens: false));
        ids.AddRange(_tokenizer.Encode(GetSystemPrompt(messages), addSpecialTokens: false));
        ids.Add(_tokenizer.ImEndId);

        foreach (var message in messages.Where(message => !string.Equals(message.Role, "system", StringComparison.OrdinalIgnoreCase)))
        {
            ids.Add(_tokenizer.ImStartId);
            ids.AddRange(_tokenizer.Encode($"{NormalizeRole(message.Role)}\n", addSpecialTokens: false));
            if (message.Parts is null || message.Parts.Count == 0)
            {
                ids.AddRange(_tokenizer.Encode(FlattenMessage(message), addSpecialTokens: false));
            }
            else
            {
                foreach (var part in message.Parts)
                {
                    if (string.Equals(part.Type, "text", StringComparison.OrdinalIgnoreCase) && !string.IsNullOrWhiteSpace(part.Text))
                    {
                        ids.AddRange(_tokenizer.Encode(part.Text, addSpecialTokens: false));
                    }
                    else if (string.Equals(part.Type, "image_url", StringComparison.OrdinalIgnoreCase) && imageIndex < mmInfo.Images.Count)
                    {
                        var image = mmInfo.Images[imageIndex++];
                        if (image.VisionInput is not null)
                        {
                            AppendVisionTokens(ids, image.VisionInput);
                        }
                    }
                    else if (string.Equals(part.Type, "video", StringComparison.OrdinalIgnoreCase) && videoIndex < mmInfo.Videos.Count)
                    {
                        var video = mmInfo.Videos[videoIndex++];
                        if (video.FrameInputs is not null)
                        {
                            ids.Add(_config.ThinkerConfig.VisionStartTokenId);
                            foreach (var frame in video.FrameInputs)
                            {
                                ids.AddRange(Enumerable.Repeat(frame.TokenIndex, frame.TokenCount));
                            }
                            ids.Add(_config.ThinkerConfig.VisionEndTokenId);
                        }
                    }
                }
            }
            ids.Add(_tokenizer.ImEndId);
        }
        ids.Add(_tokenizer.ImStartId);
        ids.AddRange(_tokenizer.Encode("assistant\n", addSpecialTokens: false));
        return ids.ToArray();
    }

