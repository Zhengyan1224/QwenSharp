    private string DecodeGeneratedTokens(Qwen25OmniForConditionalGeneration model, Tensor inputIds, GenerationConfig config)
    {
        var tokens = new List<int>();
        using var noGrad = no_grad();
        foreach (var token in TextGenerator.Generate(model, inputIds, config, _tokenizer.EosTokenId ?? -1, _tokenizer.ImEndId))
        {
            tokens.Add(token);
        }

        return _tokenizer.Decode(tokens, skipSpecialTokens: true).Trim();
    }

    private GenerationConfig BuildGenerationConfig(float? temperature, float? topP, int? maxTokens)
        => new()
        {
            MaxNewTokens = Math.Clamp(maxTokens ?? 256, 1, 1024),
            Temperature = temperature ?? 0.2f,
            TopP = topP ?? 0.9f,
            TopK = 40,
            DoSample = (temperature ?? 0.2f) > 0,
        };

    private (string AudioBase64, string? Warning) GenerateTalkerAudioBase64(
        IReadOnlyList<OpenAIMessage> messages,
        string assistantReply,
        Tensor? audioFeatures,
        string? voice)
    {
        var voiceProfile = ResolveVoiceProfile(voice, out var voiceWarning);

        if (_token2Wav is null)
        {
            if (_audioSynthesizer is not null)
            {
                var configuredAudio = _audioSynthesizer.SynthesizeToWavBase64(assistantReply, voiceProfile.Name);
                var configuredWarning = string.IsNullOrWhiteSpace(voiceWarning)
                    ? ConfiguredAudioFallbackWarning
                    : $"{voiceWarning} {ConfiguredAudioFallbackWarning}";
                return (configuredAudio, configuredWarning);
            }

            var managedAudio = ManagedSpeechSynthesizer.SynthesizeToWavBase64(assistantReply, voiceProfile.Name);
            var warning = string.IsNullOrWhiteSpace(voiceWarning)
                ? ManagedAudioFallbackWarning
                : $"{voiceWarning} {ManagedAudioFallbackWarning}";
            return (managedAudio, warning);
        }

        var synthMessages = messages.ToList();
        synthMessages.Add(new OpenAIMessage { Role = "assistant", Content = assistantReply });

        var inputIds = audioFeatures is null
            ? BuildTextPromptIds(synthMessages)
            : BuildOmniPromptIds(synthMessages, audioFeatures);

        using var noGrad = no_grad();
        using var scope = NewDisposeScope();
        using var ids = CreateInputIdsTensor(inputIds);
        using var audio = audioFeatures is null
            ? null
            : (_modelDtype is not null
                ? audioFeatures.to(_modelDtype.Value, _device)
                : audioFeatures.to(_device));
        using var logits = audio is null
            ? ((ICausalLM)_thinker).forward(ids)
            : _thinker.forward(inputIds: ids, inputFeatures: audio, positionIds: null, kvCache: null);
        using var waveform = _token2Wav.forward(logits);
        using var flatWaveform = waveform.flatten();
        using var floatWaveform = flatWaveform.to_type(ScalarType.Float32);
        using var cpuWaveform = floatWaveform.cpu();

        var flat = cpuWaveform.data<float>().ToArray();
        flat = ApplyVoiceProfile(flat, voiceProfile);
        var wavBytes = WavCodec.WritePcm16Wav(flat, 24_000);
        CollectNativeTensorMemory();
        return (Convert.ToBase64String(wavBytes), voiceWarning);
    }

