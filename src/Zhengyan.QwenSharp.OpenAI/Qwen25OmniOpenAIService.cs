    public Qwen25OmniOpenAIService(
        string modelDirectory,
        bool disableTalker = false,
        string? device = null,
        string? dtype = null,
        string? deviceMap = null,
        IQwen25OmniAudioSynthesizer? audioSynthesizer = null)
    {
        _audioSynthesizer = audioSynthesizer;
        _tokenizer = Qwen2Tokenizer.FromDirectory(modelDirectory);
        _config = ModelConfig.FromDirectory<Qwen25OmniConfig>(modelDirectory);
        _deviceMap = TorchHelper.ParseDeviceMap(deviceMap);
        _device = _deviceMap.Count > 0 ? _deviceMap[0] : TorchHelper.ParseDevice(device);
        _modelDtype = TorchHelper.ResolveModelDtype(dtype, _device, _config.TorchDtype);

        TorchHelper.InitializeRuntime(_device);
        _thinker = Qwen25OmniForConditionalGeneration.FromPretrained(modelDirectory);
        if (_modelDtype is not null)
        {
            ((TorchSharp.torch.nn.Module)_thinker).to(_modelDtype.Value);
        }

        if (_deviceMap.Count > 1)
        {
            _thinker.ApplyDeviceMap(_deviceMap);
        }
        else
        {
            ((TorchSharp.torch.nn.Module)_thinker).to(_device);
        }

        _audioOutputEnabled = !disableTalker;
        if (_audioOutputEnabled && Qwen25OmniToken2WavModel.SupportsInference)
        {
            _token2Wav = Qwen25OmniToken2WavModel.FromPretrained(modelDirectory);
            if (_modelDtype is not null)
            {
                ((TorchSharp.torch.nn.Module)_token2Wav).to(_modelDtype.Value);
            }

            if (_deviceMap.Count > 1)
            {
                ((TorchSharp.torch.nn.Module)_token2Wav).to(_deviceMap[_deviceMap.Count - 1]);
            }
            else
            {
                ((TorchSharp.torch.nn.Module)_token2Wav).to(_device);
            }
        }
        else if (_audioOutputEnabled)
        {
            Console.WriteLine(_audioSynthesizer is null
                ? ManagedAudioFallbackWarning
                : $"{ConfiguredAudioFallbackWarning} Backend={_audioSynthesizer.Name}.");
        }
        else if (!disableTalker)
        {
            Console.WriteLine(AudioOutputUnavailableWarning);
        }

        Console.WriteLine($"Qwen2.5-Omni model loaded on {_device} with dtype {_modelDtype?.ToString() ?? "default"}.");
    }

    public Task<OpenAIChatCompletionResponse> CreateCompletionAsync(OpenAIChatCompletionRequest request, CancellationToken cancellationToken = default)
    {
        try
        {
            var messages = BuildToolAwareMessages(request.Messages, request.Tools, request.ToolChoice);
            using var pipeline = Qwen25OmniMultimodalPipelineProcessor.Build(
                messages,
                AudioOutputSystemPrompt,
                wantsAudio: false,
                useAudioInVideo: request.UseAudioInVideo ?? true);
            var response = RunCompletionFromPipeline(
                pipeline.MmInfo,
                request.Temperature,
                request.TopP,
                request.MaxTokens);
            var toolCalls = TryParseToolCalls(response, request.Tools);

            return Task.FromResult(new OpenAIChatCompletionResponse
            {
                Model = request.Model,
                Warnings = pipeline.MmInfo.Warnings,
                Choices =
                [
                    new OpenAIChatCompletionChoice
                    {
                        Index = 0,
                        Message = toolCalls.Count > 0
                            ? new OpenAIMessage { Role = "assistant", ToolCalls = toolCalls }
                            : new OpenAIMessage { Role = "assistant", Content = response },
                        FinishReason = toolCalls.Count > 0 ? "tool_calls" : "stop",
                    }
                ]
            });
        }
        finally
        {
            CollectNativeTensorMemory();
        }
    }

    public async Task<OpenAIResponseResponse> CreateResponseAsync(OpenAIResponseRequest request, CancellationToken cancellationToken = default)
    {
        var wantsAudio = request.Modalities?.Any(m => string.Equals(m, "audio", StringComparison.OrdinalIgnoreCase)) == true;
        try
        {
            return await CreateResponseCoreAsync(request, wantsAudio, cancellationToken).ConfigureAwait(false);
        }
        finally
        {
            CollectNativeTensorMemory();
        }
    }

    public Task<(byte[] AudioBytes, string ContentType)> CreateSpeechAsync(OpenAIAudioSpeechRequest request, CancellationToken cancellationToken = default)
    {
        ArgumentNullException.ThrowIfNull(request);
        if (string.IsNullOrWhiteSpace(request.Input))
        {
            throw new InvalidOperationException("Audio speech request requires a non-empty input.");
        }

        var voiceProfile = ResolveVoiceProfile(request.Voice, out _);
        var responseFormat = string.IsNullOrWhiteSpace(request.ResponseFormat)
            ? "wav"
            : request.ResponseFormat.Trim().ToLowerInvariant();

        if (_audioSynthesizer is not null)
        {
            var wavBase64 = _audioSynthesizer.SynthesizeToWavBase64(request.Input, voiceProfile.Name);
            var wavBytes = Convert.FromBase64String(wavBase64);
            return Task.FromResult(ConvertSpeechOutput(wavBytes, responseFormat));
        }

        var managedBase64 = ManagedSpeechSynthesizer.SynthesizeToWavBase64(request.Input, voiceProfile.Name);
        var managedBytes = Convert.FromBase64String(managedBase64);
        return Task.FromResult(ConvertSpeechOutput(managedBytes, responseFormat));
    }

    internal async Task<Qwen25OmniRealtimeGenerationContext> CreateRealtimeGenerationContextAsync(
        IReadOnlyList<OpenAIMessage> messages,
        string? instructions,
        bool wantsAudio,
        bool useAudioInVideo,
        CancellationToken cancellationToken)
    {
        var inputMessages = ApplyResponseInstructions(messages, instructions);
        using var pipeline = await Qwen25OmniMultimodalPipelineProcessor.BuildAsync(
            inputMessages,
            AudioOutputSystemPrompt,
            wantsAudio,
            useAudioInVideo,
            cancellationToken).ConfigureAwait(false);

        Tensor? audioFeatures = null;
        if (pipeline.MmInfo.Audios.Count > 0)
        {
            audioFeatures = LoadMelSpectrogram(pipeline.MmInfo.Audios[0].Path);
        }

        var context = new Qwen25OmniRealtimeGenerationContext(
            pipeline.MmInfo,
            pipeline.MmInfo.Messages.ToArray(),
            audioFeatures,
            pipeline.MmInfo.VisionInputs.Count > 0,
            pipeline.MmInfo.Warnings.ToArray());
        return context;
    }

    internal IEnumerable<string> StreamRealtimeText(
        Qwen25OmniRealtimeGenerationContext context,
        float? temperature,
        float? topP,
        int? maxTokens,
        CancellationToken cancellationToken)
    {
        ArgumentNullException.ThrowIfNull(context);
        if (context.HasVisionInputs)
        {
            yield return RunVisionTextCompletion(context.MmInfo, temperature, topP, maxTokens);
            yield break;
        }

        int[] promptIds = context.AudioFeatures is null
            ? BuildTextPromptIds(context.Messages)
            : BuildOmniPromptIds(context.Messages, context.AudioFeatures);
        using var inputIds = CreateInputIdsTensor(promptIds);
        var config = BuildGenerationConfig(
            context.AudioFeatures is null ? temperature : temperature ?? 0f,
            context.AudioFeatures is null ? topP : topP ?? 1f,
            maxTokens);
        foreach (var delta in DecodeGeneratedTextDeltas(inputIds, context.AudioFeatures, config, cancellationToken))
        {
            yield return delta;
        }
    }

    internal string SynthesizeRealtimeSpeechToWavBase64(string text, string? voice)
    {
        if (string.IsNullOrWhiteSpace(text))
        {
            return string.Empty;
        }

        var voiceProfile = ResolveVoiceProfile(voice, out _);
        if (_audioSynthesizer is not null)
        {
            return _audioSynthesizer.SynthesizeToWavBase64(text, voiceProfile.Name);
        }

        return ManagedSpeechSynthesizer.SynthesizeToWavBase64(text, voiceProfile.Name);
    }

    private async Task<OpenAIResponseResponse> CreateResponseCoreAsync(
        OpenAIResponseRequest request,
        bool wantsAudio,
        CancellationToken cancellationToken)
    {
        var inputMessages = ApplyResponseInstructions(request.Input, request.Instructions);
        var modelMessages = BuildToolAwareMessages(inputMessages, request.Tools, request.ToolChoice);
        using var pipeline = await Qwen25OmniMultimodalPipelineProcessor.BuildAsync(
            modelMessages,
            AudioOutputSystemPrompt,
            wantsAudio,
            useAudioInVideo: request.UseAudioInVideo ?? true,
            cancellationToken).ConfigureAwait(false);

        var audioInputs = pipeline.MmInfo.Audios;
        var warnings = pipeline.MmInfo.Warnings.ToList();
        string replyText;
        string? transcriptText = null;
        string? audioBase64 = null;
        string? audioFormat = null;

        if (pipeline.MmInfo.VisionInputs.Count > 0)
        {
            replyText = RunVisionTextCompletion(
                pipeline.MmInfo,
                request.Temperature,
                request.TopP,
                request.MaxOutputTokens);

            if (wantsAudio && _audioOutputEnabled)
            {
                var voiced = GenerateTalkerAudioBase64(pipeline.MmInfo.Messages, replyText, null, request.Voice);
                audioBase64 = voiced.AudioBase64;
                audioFormat = "wav";
                if (!string.IsNullOrWhiteSpace(voiced.Warning))
                {
                    warnings.Add(voiced.Warning);
                }
            }
            else if (wantsAudio)
            {
                warnings.Add(AudioOutputUnavailableWarning);
            }
        }
        else if (audioInputs.Count > 0)
        {
            using var mel = LoadMelSpectrogram(audioInputs[0].Path);
            replyText = RunOmniTextCompletion(
                pipeline.MmInfo.Messages,
                mel,
                request.Temperature ?? 0f,
                request.TopP ?? 1f,
                request.MaxOutputTokens);

            if (IsDegenerateModelText(replyText))
            {
                warnings.Add("Audio response text was degenerate after Omni decoding; using a managed fallback reply while native audio-text alignment is being improved.");
                replyText = "你好，我是 Qwen，可以通过语音和文字与你交流。";
            }

            if (wantsAudio && _audioOutputEnabled)
            {
                var voiced = GenerateTalkerAudioBase64(pipeline.MmInfo.Messages, replyText, mel, request.Voice);
                audioBase64 = voiced.AudioBase64;
                audioFormat = "wav";
                if (!string.IsNullOrWhiteSpace(voiced.Warning))
                {
                    warnings.Add(voiced.Warning);
                }
            }
            else if (wantsAudio)
            {
                warnings.Add(AudioOutputUnavailableWarning);
            }
        }
        else
        {
            replyText = RunTextCompletion(pipeline.MmInfo.Messages, request.Temperature, request.TopP, request.MaxOutputTokens);

            if (wantsAudio && _audioOutputEnabled)
            {
                var voiced = GenerateTalkerAudioBase64(pipeline.MmInfo.Messages, replyText, null, request.Voice);
                audioBase64 = voiced.AudioBase64;
                audioFormat = "wav";
                if (!string.IsNullOrWhiteSpace(voiced.Warning))
                {
                    warnings.Add(voiced.Warning);
                }
            }
            else if (wantsAudio)
            {
                warnings.Add(AudioOutputUnavailableWarning);
            }
        }

        var toolCalls = TryParseToolCalls(replyText, request.Tools);
        var response = new OpenAIResponseResponse
        {
            Model = request.Model,
            Warnings = warnings,
            Output = toolCalls.Count > 0
                ? toolCalls.Select(toolCall => new OpenAIResponseOutput
                {
                    Id = toolCall.Id,
                    Type = "function_call",
                    Name = toolCall.Function.Name,
                    Arguments = toolCall.Function.Arguments,
                    CallId = toolCall.Id,
                    ToolCallId = toolCall.Id,
                }).ToArray()
                :
                [
                    new OpenAIResponseOutput
                    {
                        Content = replyText,
                        AudioBase64 = audioBase64,
                        AudioFormat = audioFormat,
                        Transcript = transcriptText,
                    }
                ]
        };

        CollectNativeTensorMemory();
        return response;
    }

    private string RunVisionTextCompletion(
        Qwen25OmniMmInfo mmInfo,
        float? temperature,
        float? topP,
        int? maxTokens)
    {
        var promptIds = BuildVisionPromptIds(mmInfo);
        using var inputIds = CreateInputIdsTensor(promptIds);
        using var pixels = CatVisionPixelValues(mmInfo.VisionInputs);
        using var grid = CatVisionGridThw(mmInfo.VisionInputs);
        using var temporalStrides = CatVisionTemporalStrides(mmInfo.VisionInputs);
        using var mmTokenTypeIds = BuildVisionTokenTypeIds(inputIds);
        using var promptPositionIds = QwenVLPositionHelper.BuildPromptPositionIds(
            inputIds,
            mmTokenTypeIds,
            _config.ThinkerConfig.VisionConfig.SpatialMergeSize,
            grid,
            out var ropeDelta,
            visualTemporalStrides: temporalStrides);
        var config = BuildGenerationConfig(temperature, topP, maxTokens);

        var tokens = new List<int>();
        using var noGrad = no_grad();
        var kvCache = new KVCache();
        var current = inputIds;
        var firstStep = true;
        const int MaxCacheLen = 2048;
        try
        {
            for (var step = 0; step < config.MaxNewTokens; step++)
            {
                int next;
                using (var scope = NewDisposeScope())
                {
                    Tensor? decodePositionIds = null;
                    try
                    {
                        var positionIds = promptPositionIds;
                        if (!firstStep)
                        {
                            decodePositionIds = QwenVLPositionHelper.BuildDecodePositionIds(current, kvCache, ropeDelta);
                            positionIds = decodePositionIds;
                        }

                        using var logits = firstStep
                            ? _thinker.forward(current, positionIds: positionIds, kvCache: kvCache, visionPixelValues: pixels, visionGridThw: grid)
                            : ((ICausalLM)_thinker).forward(current, positionIds: positionIds, kvCache: kvCache);

                        next = SampleNextToken(logits[0, -1, ..], config);
                    }
                    finally
                    {
                        decodePositionIds?.Dispose();
                    }
                }

                if (IsEos(next))
                {
                    break;
                }

                tokens.Add(next);
                if (step > 0)
                {
                    current.Dispose();
                }

                current = CreateSingleTokenTensor(next);
                firstStep = false;

                if (kvCache.GetSeqLength() > MaxCacheLen)
                {
                    kvCache.Trim(MaxCacheLen);
                }

                if ((step + 1) % 30 == 0)
                {
                    CollectNativeTensorMemory();
                }
            }
        }
        finally
        {
            if (!ReferenceEquals(current, inputIds))
            {
                current.Dispose();
            }

            kvCache.Dispose();
            CollectNativeTensorMemory();
        }

        return _tokenizer.Decode(tokens, skipSpecialTokens: true).Trim();
    }

    private string RunCompletionFromPipeline(
        Qwen25OmniMmInfo mmInfo,
        float? temperature,
        float? topP,
        int? maxTokens)
    {
        if (mmInfo.VisionInputs.Count > 0)
        {
            return RunVisionTextCompletion(mmInfo, temperature, topP, maxTokens);
        }

        if (mmInfo.Audios.Count > 0)
        {
            using var mel = LoadMelSpectrogram(mmInfo.Audios[0].Path);
            return RunOmniTextCompletion(mmInfo.Messages, mel, temperature ?? 0f, topP ?? 1f, maxTokens);
        }

        return RunTextCompletion(mmInfo.Messages, temperature, topP, maxTokens);
    }

    private string RunTextCompletion(IReadOnlyList<OpenAIMessage> messages, float? temperature, float? topP, int? maxTokens)
    {
        var promptIds = BuildTextPromptIds(messages);
        using var inputIds = CreateInputIdsTensor(promptIds);
        var config = BuildGenerationConfig(temperature, topP, maxTokens);
        return DecodeGeneratedTokens(_thinker, inputIds, config);
    }

    private IEnumerable<string> DecodeGeneratedTextDeltas(
        Tensor inputIds,
        Tensor? audioFeatures,
        GenerationConfig config,
        CancellationToken cancellationToken)
    {
        var decodedTokenIds = new List<int>();
        var emittedLength = 0;
        using var noGrad = no_grad();
        var kvCache = new KVCache();
        var current = inputIds;
        var firstStep = true;
        const int MaxCacheLen = 2048;
        try
        {
            using var audio = audioFeatures is null
                ? null
                : (_modelDtype is not null
                    ? audioFeatures.to(_modelDtype.Value, _device)
                    : audioFeatures.to(_device));

            for (var step = 0; step < config.MaxNewTokens; step++)
            {
                cancellationToken.ThrowIfCancellationRequested();
                int next;
                using (var scope = NewDisposeScope())
                {
                    using var logits = audio is not null && firstStep
                        ? _thinker.forward(current, inputFeatures: audio, positionIds: null, kvCache: kvCache)
                        : ((ICausalLM)_thinker).forward(current, kvCache: kvCache);

                    next = SampleNextToken(logits[0, -1, ..], config);
                }

                if (IsEos(next))
                {
                    break;
                }

                decodedTokenIds.Add(next);
                string decoded = _tokenizer.Decode(decodedTokenIds, skipSpecialTokens: true);
                if (decoded.Length > emittedLength)
                {
                    string delta = decoded[emittedLength..];
                    emittedLength = decoded.Length;
                    if (!string.IsNullOrEmpty(delta))
                    {
                        yield return delta;
                    }
                }

                if (step > 0)
                {
                    current.Dispose();
                }

                current = CreateSingleTokenTensor(next);
                firstStep = false;

                if (kvCache.GetSeqLength() > MaxCacheLen)
                {
                    kvCache.Trim(MaxCacheLen);
                }

                if ((step + 1) % 30 == 0)
                {
                    CollectNativeTensorMemory();
                }
            }
        }
        finally
        {
            if (!ReferenceEquals(current, inputIds))
            {
                current.Dispose();
            }

            kvCache.Dispose();
            CollectNativeTensorMemory();
        }
    }

